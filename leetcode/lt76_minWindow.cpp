// 双指针和滑动窗口的区别：
// 双指针不需要记录指针之间的内容；
// 滑动窗口需要记录指针之间的内容，根据内容来判断，
// 那个指针移动，通畅使用unorder_map记录。
class Solution {
 public:
  string minWindow(string s, string t) {
    if (s.size() < t.size()) return "";
    unordered_map<char, int> count;
    for (auto&& c : t) {
      count[c]++;
    }

    int start = 0;
    int len = 0;
    int minLen = INT_MAX;
    int minPos = -1;
    while (start + len <= s.size()) {
      bool cover = true;
      for (auto&& it : count) {
        if (it.second > 0) {
          cover = false;
          break;
        }
      }
      if (cover && len < minLen) {
        minLen = len;
        minPos = start;
      }

      if (!cover) {
        len++;
        auto it = count.find(s[start + len - 1]);
        if (it != count.end()) it->second -= 1;
      } else {
        auto it = count.find(s[start]);
        if (it != count.end()) it->second += 1;
        start++;
        len--;
      }
    }

    bool cover = true;
    for (auto&& it : count) {
      if (it.second > 0) {
        cover = false;
        break;
      }
    }
    if (cover && len < minLen) {
      minLen = len;
      minPos = start;
    }
    return minLen == INT_MAX ? "" : s.substr(minPos, minLen);
  }
};

